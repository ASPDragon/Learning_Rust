#[derive(Debug)]
struct Rectangle {
    width: u32,
    height: u32,
}

impl Rectangle {
    fn square(dimension: u32) -> Self {
        Self {
            width: dimension,
            height: dimension,
        }
    }

    fn area(&self) -> u32 {
        self.width * self.height
    }

    fn can_fit(&self, rhs: &Rectangle) -> bool {
        (self.width > rhs.width && self.height > rhs.height) ||
            (self.height > rhs.width && self.width > rhs.height)
    }
}

